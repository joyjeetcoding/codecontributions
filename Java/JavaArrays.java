public class JavaArrays {
    public static void main(String[] args) {
        int marks[] = new int[5];
        marks[0] = 23;
        marks[1] = 24;
        marks[2] = 25;
        marks[3] = 26;

        for(int i = 0; i < 4; i++)
        {
            System.out.println(marks[i]);
            System.out.println('\n');
        }
    }
}